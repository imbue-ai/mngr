"""The catalog of fields usable by ``mngr list``.

This is the single source of truth for the fields that can be referenced in the
three ``mngr list`` field contexts:

- CEL expressions for ``--include`` / ``--exclude`` (filtering)
- CEL expressions for ``--sort`` (ordering)
- ``--fields`` / ``--format`` template strings (selecting / formatting output)

Most of the catalog is derived *live* from the real data shape -- walking
``AgentDetails`` (recursing into ``HostDetails`` and the nested resource / ssh
models) so every agent and host field, with its declared type and description,
appears automatically and can never drift from the models.

The remainder cannot be derived from the data shape because it is not in any
model: the computed fields (``age`` / ``runtime`` / ``idle``) produced by
``agent_details_to_cel_context``, the documented aliases (``host.provider``,
``project``), and the dynamic key patterns (``labels.$KEY``, ``plugin.*``).
Those are listed explicitly in ``_EXTRA_ROWS`` below. The completeness of that
hand-written list is pinned to reality by tests in ``field_catalog_test.py``:
``_CEL_SYNTHESIZED_KEYS`` is cross-checked against what ``agent_details_to_cel_context``
actually emits, and the alias rows against ``_FIELD_ALIASES``.

``mngr list --schema`` renders this catalog directly; the ``list`` command's
help "Available Fields" section is rendered from it too, so the docs (including
the static markdown on GitHub generated by ``scripts/make_cli_docs.py``) stay
in lockstep with the models.
"""

import typing
from enum import Enum
from typing import Any

from imbue.imbue_common.frozen_model import FrozenModel
from imbue.mngr.interfaces.data_types import AgentDetails
from imbue.mngr.utils.model_schema import render_annotation
from imbue.mngr.utils.model_schema import walk_model_fields


class FieldContext(str, Enum):
    """A context in which a list field name may be referenced.

    There are only two, because ``--include``/``--exclude`` and ``--sort`` all
    evaluate CEL against the *same* context (see ``agent_details_to_cel_context``),
    so a name usable in a filter is always usable in a sort and vice versa --
    they are one ``CEL`` context, not two. ``TEMPLATE`` is the genuinely separate
    ``--fields`` / ``--format`` resolution path.
    """

    CEL = "cel"
    TEMPLATE = "template"


# Every field is usable in CEL (``--include``/``--exclude``/``--sort``); the only
# axis that varies is whether it is *also* usable in ``--fields`` / ``--format``
# templates. The computed fields (age/runtime/idle) are CEL-only because the
# template path cannot derive them.
ALL_CONTEXTS: tuple[FieldContext, ...] = (FieldContext.CEL, FieldContext.TEMPLATE)
CEL_CONTEXTS: tuple[FieldContext, ...] = (FieldContext.CEL,)


class FieldSection(str, Enum):
    """Display grouping for a catalog field in help / schema output."""

    AGENT = "Agent fields"
    HOST = "Host fields"
    COMPUTED = "Computed and alias fields"
    DYNAMIC = "Dynamic fields"


# Order in which sections are rendered in the generated help text.
_SECTION_ORDER: tuple[FieldSection, ...] = (
    FieldSection.AGENT,
    FieldSection.HOST,
    FieldSection.COMPUTED,
    FieldSection.DYNAMIC,
)


class FieldCatalogRow(FrozenModel):
    """One referenceable ``mngr list`` field."""

    key: str
    type: str
    description: str
    contexts: tuple[FieldContext, ...]
    section: FieldSection


# Keys that ``agent_details_to_cel_context`` synthesizes on top of the raw
# ``AgentDetails`` model dump -- the computed fields (age/runtime/idle) and the
# CEL aliases (host.provider, project). Kept as a named constant so the
# cross-check test can assert the live computation produces exactly these (and
# the rows below stay in sync with it).
_CEL_SYNTHESIZED_KEYS: tuple[str, ...] = ("age", "runtime", "idle", "host.provider", "project")


# Fields that are not part of the model shape and so must be described by hand:
# the CEL-computed fields, the documented aliases, and the dynamic key patterns.
_EXTRA_ROWS: tuple[FieldCatalogRow, ...] = (
    FieldCatalogRow(
        key="age",
        type="float",
        description="Seconds since create_time.",
        contexts=CEL_CONTEXTS,
        section=FieldSection.COMPUTED,
    ),
    FieldCatalogRow(
        key="runtime",
        type="float",
        description="Alias for runtime_seconds.",
        contexts=CEL_CONTEXTS,
        section=FieldSection.COMPUTED,
    ),
    FieldCatalogRow(
        key="idle",
        type="float",
        description=(
            "Seconds since the most recent activity across user_activity_time, "
            "agent_activity_time, and host.ssh_activity_time (only present when at least one is set)."
        ),
        contexts=CEL_CONTEXTS,
        section=FieldSection.COMPUTED,
    ),
    FieldCatalogRow(
        key="host.provider",
        type="ProviderInstanceName",
        description="Alias for host.provider_name (the documented short form).",
        contexts=ALL_CONTEXTS,
        section=FieldSection.COMPUTED,
    ),
    FieldCatalogRow(
        key="project",
        type="str",
        description="Alias for labels.project (mirrors the --project filter flag).",
        contexts=ALL_CONTEXTS,
        section=FieldSection.COMPUTED,
    ),
    FieldCatalogRow(
        key="labels.$KEY",
        type="str",
        description="A specific agent label value (e.g. labels.project).",
        contexts=ALL_CONTEXTS,
        section=FieldSection.DYNAMIC,
    ),
    FieldCatalogRow(
        key="plugin.$PLUGIN_NAME.*",
        type="Any",
        description="Plugin-defined agent fields (e.g. plugin.chat_history.messages).",
        contexts=ALL_CONTEXTS,
        section=FieldSection.DYNAMIC,
    ),
    FieldCatalogRow(
        key="host.tags.$KEY",
        type="str",
        description="A specific host label/tag value (e.g. host.tags.env).",
        contexts=ALL_CONTEXTS,
        section=FieldSection.DYNAMIC,
    ),
    FieldCatalogRow(
        key="host.plugin.$PLUGIN_NAME.*",
        type="Any",
        description="Host plugin fields (e.g. host.plugin.aws.iam_user).",
        contexts=ALL_CONTEXTS,
        section=FieldSection.DYNAMIC,
    ),
)


def _is_discriminator_annotation(annotation: Any) -> bool:
    """Whether ``annotation`` is a single-value ``Literal`` discriminator tag.

    A one-value ``Literal`` (e.g. ``Literal["agent"]``) can only ever hold that
    one constant, so it is a model serialization discriminator rather than a
    user-referenceable field -- filtering, sorting, or formatting on it is
    meaningless. Literals with more than one arm are genuine enumerations of
    user-facing values and are *not* treated as discriminators.
    """
    return typing.get_origin(annotation) is typing.Literal and len(typing.get_args(annotation)) == 1


def build_list_field_catalog() -> tuple[FieldCatalogRow, ...]:
    """Build the full catalog: live model fields followed by the hand-listed extras.

    Walks ``AgentDetails`` recursively (through ``HostDetails`` and the optional
    resource / ssh sub-models) so every agent and host field is derived from the
    real data shape, then appends ``_EXTRA_ROWS`` for the fields the shape can't
    describe. Single-value ``Literal`` discriminator tags (e.g.
    ``AgentDetails.resource_type``) are skipped, since they are constant model
    serialization markers rather than referenceable fields.
    """
    rows: list[FieldCatalogRow] = []
    for key, annotation, description in walk_model_fields(
        AgentDetails,
        recurse_optional=True,
        recurse_sequence=False,
        emit_container_rows=True,
    ):
        if _is_discriminator_annotation(annotation):
            continue
        # The bare ``host`` container row has no trailing dot, so match it explicitly
        # alongside its dotted descendants (host.name, host.resource, ...).
        section = FieldSection.HOST if key == "host" or key.startswith("host.") else FieldSection.AGENT
        rows.append(
            FieldCatalogRow(
                key=key,
                type=render_annotation(annotation),
                description=description,
                contexts=ALL_CONTEXTS,
                section=section,
            )
        )
    rows.extend(_EXTRA_ROWS)
    return tuple(rows)


def catalog_rows_as_dicts() -> list[dict[str, str]]:
    """Render the catalog as JSON/template-friendly dict rows.

    ``contexts`` is collapsed to a comma-joined string so the rows compose with
    ``--format`` template strings (which expand flat string fields).
    """
    return [
        {
            "key": row.key,
            "type": row.type,
            "description": row.description,
            "contexts": ", ".join(context.value for context in row.contexts),
            "section": row.section.value,
        }
        for row in build_list_field_catalog()
    ]


def render_catalog_help_markdown() -> str:
    """Render the catalog as the markdown 'Available Fields' help section.

    Grouped by section, each field annotated with the contexts in which it is
    usable, so the help text is generated from -- and cannot drift from -- the
    live model shape.
    """
    rows = build_list_field_catalog()
    lines: list[str] = [
        "Every field below can be used in CEL expressions for `--include`/`--exclude` and "
        "`--sort` (which share one evaluation context).",
        "",
        "Fields marked `(cel only)` cannot be used in `--fields`/`--format` template strings; "
        "all others can. (Only the computed fields age/runtime/idle are `cel`-only.)",
        "",
    ]
    for section in _SECTION_ORDER:
        section_rows = [row for row in rows if row.section == section]
        if not section_rows:
            continue
        lines.append(f"**{section.value}:**")
        for row in section_rows:
            marker = "" if FieldContext.TEMPLATE in row.contexts else " `(cel only)`"
            description = f" - {row.description}" if row.description else ""
            lines.append(f"- `{row.key}`{marker}{description}")
        lines.append("")
    lines.append("**Notes:**")
    lines.append(
        "- You can use Python-style list slicing for list fields "
        "(e.g. `host.snapshots[0]` for the first snapshot, `host.snapshots[:3]` for the first 3)."
    )
    return "\n".join(lines)
